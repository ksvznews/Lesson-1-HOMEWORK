public class Task4 {
public static void main(String[] args) {
    // Зарплаты сотрудников
    int Employee1 = 55000;
    int Employee2 = 40000;
    int Employee3 = 70000;

    // Изменение зарплат сотрудников
    // 1 сотрудник: увеличение в 2 раза
    Employee1 *= 2;

    // 2 сотрудник: увеличение на 15000
    Employee2 += 15000;

    // Вывод новых зарплат в консоль
    System.out.println("Новая зарплата 1 сотрудника: " + Employee1);
    System.out.println("Новая зарплата 2 сотрудника: " + Employee2);
    System.out.println("Новая зарплата 3 сотрудника: " + Employee3);
}
}
