public class Task3{
    public static void main(String[] args) {
        // Создание переменных для данных о работнике
        String firstName = "Жаслан"; // Имя
        String lastName = "Касенов"; // Фамилия
        String middleName = "Амиржанович"; // Отчество
        int age = 30; // Возраст
        String city = "Астана"; // Город

        // Изменение возраста сотрудника
        age = 27;

        // Вывод данных о работнике в консоль
        System.out.println("Имя: " + firstName);
        System.out.println("Фамилия: " + lastName);
        System.out.println("Отчество: " + middleName);
        System.out.println("Возраст: " + age);
        System.out.println("Город: " + city);

        //Данный код создает переменные для данных о работнике, затем изменяет значение переменной age и
        // выводит все переменные в консоль.
    }
}
